    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try{            
            DB::beginTransaction();
            $bancoList = Banco_Lista::findOrFail($id);
            $bancoList->banco_lista_nombre = $request->get('banco_lista_nombre');            
            if ($request->get('banco_lista_estado') == "on"){
                $bancoList->banco_lista_estado = 1;
            }else{
                $bancoList->banco_lista_estado = 0;
            }
            $bancoList->save();       
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion de banco lista -> '.$request->get('banco_lista_nombre'),'0','');
            /*Fin de registro de auditoria */   
            DB::commit();
            return redirect('bancoLista')->with('success','Datos actualizados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('bancoLista')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

