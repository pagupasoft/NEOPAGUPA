    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{            
            DB::beginTransaction();
            $bancoList = new Banco_Lista();
            $bancoList->banco_lista_nombre = $request->get('banco_lista_nombre');            
            $bancoList->empresa_id = Auth::user()->empresa_id;
            $bancoList->banco_lista_estado = 1;
            $bancoList->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro de banco lista -> '.$request->get('banco_lista_nombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('bancoLista')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('bancoLista')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

