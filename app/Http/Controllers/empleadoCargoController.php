    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{            
            DB::beginTransaction();
            $cargo = new Empleado_Cargo();
            $cargo->empleado_cargo_nombre = $request->get('empleado_cargo_nombre');           
            $cargo->empresa_id = Auth::user()->empresa_id;
            $cargo->empleado_cargo_estado = 1;
            $cargo->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro de un cargo -> '.$request->get('empleado_cargo_nombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('empleadoCargo')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('empleadoCargo')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

