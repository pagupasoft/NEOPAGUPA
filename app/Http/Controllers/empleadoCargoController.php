    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try{            
            DB::beginTransaction();
            $cargo = Empleado_Cargo::findOrFail($id);
            $cargo->empleado_cargo_nombre = $request->get('empleado_cargo_nombre');
            if ($request->get('empleado_cargo_estado') == "on"){
                $cargo->empleado_cargo_estado = 1;
            }else{
                $cargo->empleado_cargo_estado = 0;
            }
            $cargo->save();       
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion de cargo -> '.$request->get('empleado_cargo_nombre'),'0','');
            /*Fin de registro de auditoria */   
            DB::commit();
            return redirect('empleadoCargo')->with('success','Datos actualizados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('empleadoCargo')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

