    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try{            
            DB::beginTransaction();
            $formaPago = Forma_Pago::findOrFail($id);
            $formaPago->forma_pago_nombre = $request->get('forma_pago_nombre');
            $formaPago->forma_pago_codigo = $request->get('forma_pago_codigo');          
            if ($request->get('forma_pago_estado') == "on"){
                $formaPago->forma_pago_estado = 1;
            }else{
                $formaPago->forma_pago_estado = 0;
            }
            $formaPago->save();       
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion forma de pago -> '.$request->get('forma_pago_nombre').' con codigo -> '.$request->get('forma_pago_codigo'),'0','');
            /*Fin de registro de auditoria */   
            DB::commit();
            return redirect('formaPago')->with('success','Datos actualizados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('formaPago')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

