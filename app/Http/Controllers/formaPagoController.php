    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{            
            DB::beginTransaction();
            $formaPago = new Forma_Pago();
            
            $formaPago->forma_pago_nombre = $request->get('forma_pago_nombre');
            $formaPago->forma_pago_codigo = $request->get('forma_pago_codigo');
            $formaPago->empresa_id = Auth::user()->empresa_id;
            $formaPago->forma_pago_estado = 1;
            $formaPago->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro forma de pago -> '.$request->get('forma_pago_nombre').' con codigo -> '.$request->get('forma_pago_codigo'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('formaPago')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('formaPago')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

