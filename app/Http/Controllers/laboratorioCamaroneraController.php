    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{
            DB::beginTransaction();
            $laboratorio = new Laboratorio_Camaronera();
            $laboratorio->laboratorio_nombre = $request->get('idNombre');    
            $laboratorio->laboratorio_Ubicacion = $request->get('idUbicacion');              
            $laboratorio->laboratorio_estado  = 1;
            $laboratorio->empresa_id = Auth::user()->empresa_id;       
            $laboratorio->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro de Laboratorio de Camaronera -> '.$request->get('idNombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('laboratorioC')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('laboratorioC')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

