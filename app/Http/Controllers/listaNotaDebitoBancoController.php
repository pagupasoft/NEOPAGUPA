    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try{
            DB::beginTransaction();
            $debitoBanco = Nota_Debito_banco::findOrFail($id);
            $general = new generalController();
            $cierre = $general->cierre($debitoBanco->nota_fecha);          
            if($cierre){
                return redirect('listanotaDebitoBancario')->with('error2','No puede realizar la operacion por que pertenece a un mes bloqueado');
            }
                

            foreach($debitoBanco->diario->detalles as $detalle){                            
                $detalle->delete();
                $general->registrarAuditoria('Eliminacion de detalles de diario de nota de debito bancario numero: -> '.$debitoBanco->nota_numero, $id, '');
            } 
            foreach($debitoBanco->detallesTipoMovimiento as $detalleTip){                            
                $detalleTip->delete();
                $general->registrarAuditoria('Eliminacion de detalles de tipo de movimiento-> '.$debitoBanco->nota_numero, $id, '');
            }
               
            $debito= Nota_Debito_banco::findOrFail($id);
            $debito->diario_id=null;    
            $debito->save();   
                
            $diario = Diario::findOrFail($debitoBanco->diario->diario_id);
            $debitoBanco->diario->delete();                        
            $general->registrarAuditoria('Eliminacion de Diario de  nota de debito bancario: -> '.$debitoBanco->nota_numero, $id, 'Con el diario-> '.$diario->diario_codigo.'Comentario -> '.$diario->diario_comentario);
                                    
                $aux=$debitoBanco;
                $debitoBanco->delete();  
                $general->registrarAuditoria('Eliminacion de  nota de debito bancario: -> '.$aux->nota_numero, $id, 'Con Valor -> '.$aux->nota_valor.' Descripcion -> '.$aux->nota_descripcion);
              DB::commit();
              return redirect('listanotaDebitoBancario')->with('success','Datos Eliminados exitosamente');            
            }catch(\Exception $ex){
                DB::rollBack();
                return redirect('listanotaDebitoBancario')->with('error','El registro no pudo ser borrado, tiene resgitros adjuntos.');
            } 
    }
