    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{
            DB::beginTransaction();
            $nauplio = new Nauplio();
            $nauplio->nauplio_nombre = $request->get('idNombre');             
            $nauplio->nauplio_estado  = 1;
            $nauplio->laboratorio_id = $request->get('idLaboratorio');  
            $nauplio->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro de Nauplio -> '.$request->get('idNombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('nauplio/'.$request->get('idLaboratorio').'/agregar')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('nauplio/'.$request->get('idLaboratorio').'/agregar')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

