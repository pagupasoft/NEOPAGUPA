    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try{
            DB::beginTransaction();
            $rol=Rol::findOrFail($id);
            $rol->rol_nombre=$request->get('idNombre');
            $rol->rol_tipo=$request->get('idTipo');
            if ($request->get('idEstado') == "on"){   
                $rol->rol_estado=1;
            }else{
                $rol->rol_estado=0;
            }
            $rol->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion de rol -> '.$request->get('idNombre'),'0','Rol con id -> '.$id);
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('rol')->with('success','Datos actualizados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('rol')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

