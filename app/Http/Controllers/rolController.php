     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{
            DB::beginTransaction();
            $rol = new Rol();
            $rol->rol_nombre=$request->get('idNombre');
            $rol->rol_tipo=$request->get('idTipo');
            $rol->empresa_id = Auth::user()->empresa_id;
