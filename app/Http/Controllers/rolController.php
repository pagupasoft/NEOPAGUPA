    public function guardarPermisos(Request $request, $id)
    {
        try {
            DB::beginTransaction();
            $permisosAsignados='';
            $rol_permiso=Rol_Permiso::where('rol_id','=',$id)->delete();
            $permisos=DB::table('permiso')->where('permiso_estado','=','1')->orderBy('permiso_nombre','asc')->get();
            foreach ($permisos as $permiso) {  
                if($request->get($permiso->permiso_id) == "on"){
                    $rol_permiso= new Rol_Permiso;
                    $rol_permiso->permiso_id=$permiso->permiso_id;
                    $rol_permiso->rol_id=$id;
                    $rol_permiso->save();
                    $permisosAsignados=$permisosAsignados.'-'.$permiso->permiso_nombre;
                }
            }
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion de permisos de rol con id -> '.$id,'0','Los permisos asignados  fueron -> '.$permisosAsignados);
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('rol')->with('success','Datos guardados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('rol')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
