    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try{
            DB::beginTransaction();
            $tipoMedicamento = Tipo_Medicamento::findOrFail($id);
            $tipoMedicamento->tipo_nombre = $request->get('idNombre');        
            if ($request->get('idEstado') == "on"){
                $tipoMedicamento->tipo_estado = 1;
            }else{
                $tipoMedicamento->tipo_estado = 0;
            }
            $tipoMedicamento->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Actualizacion de tipo de Medicamento -> '.$request->get('idNombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('tipoMedicamento')->with('success','Datos actualizados exitosamente');
        }catch(\Exception $ex){
            DB::rollBack();
            return redirect('tipoMedicamento')->with('error2','Ocurrio un error en el procedimiento. Vuelva a intentar. ('.$ex->getMessage().')');
        }
    }

