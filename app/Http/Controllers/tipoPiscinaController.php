    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try{
            DB::beginTransaction();
            $tipo = new Tipo_Piscina();
            $tipo->tipo_nombre = $request->get('idNombre');             
            $tipo->tipo_estado  = 1;
            $tipo->empresa_id = Auth::user()->empresa_id;       
            $tipo->save();
            /*Inicio de registro de auditoria */
            $auditoria = new generalController();
            $auditoria->registrarAuditoria('Registro de tipo de Piscina -> '.$request->get('idNombre'),'0','');
            /*Fin de registro de auditoria */
            DB::commit();
            return redirect('tipoPiscina')->with('success','Datos guardados exitosamente');
